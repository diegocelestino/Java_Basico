public class MeuPrimeiroPrograma {
    public static void main(String[] args){

        calc(10, 5);


        System.out.println();
    }
    static void calc(int a, int b){

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
    }
}
